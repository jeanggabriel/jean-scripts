n1 = int(input('Digite Um Numero: '))
n2 = int(input('Outro Numero: '))
print('\n'.format(n1,n2))
s = n1+n2 #soma
m = n1*n2 #multiplicaçao
d = float(n1/n2) #divisao
di = n1//n2 #divisao inteira
e = float(n1**n2) #exponenciação
print('A soma É {} >>>\n\n A Multiplicação É {} >>>\n\n A divisão É {:.2f} >>>\n\n'.format(s,m,d),end='')
print('A divisão Inteira É {} >>>\n\n A Exponenciação É {:.2f} >>>\n\n'.format(di, e))
print('\n----------------AULA 07 OPERADORES ARITMÉTICOS ------------------- \n')



